export function getRandomWord(categoryId: string): string | null {
  const category = wordCategories.find((c) => c.id === categoryId);
  if (!category) return null;

  const randomIndex = Math.floor(Math.random() * category.words.length);
  return category.words[randomIndex];
}

export function getCategoryById(categoryId: string): WordCategory | undefined {
  return wordCategories.find((c) => c.id === categoryId);
}

export function getAllCategoryIds(): string[] {
  return wordCategories.map((c) => c.id);
}
